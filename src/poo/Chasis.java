package poo;

public class Chasis {
    int ruedas;
    int largo;
    int ancho;
    int motor;
    int peso;

    public Chasis(int ruedas, int largo, int ancho, int motor, int peso) {
        this.ruedas = ruedas;
        this.largo = largo;
        this.ancho = ancho;
        this.motor = motor;
        this.peso = peso;
    }

}
